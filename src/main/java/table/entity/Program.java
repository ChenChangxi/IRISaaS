package table.entity;

public class Program {

    private String no;

    private String name;

    private String level;

    private String type;

    private String major;

    private String infoStart;

    private String infoEnd;

    private String infoCorporate;

    public String getNo() {

        return no;
    }

    public void setNo(String no) {

        this.no = no;
    }

    public String getName() {

        return name;
    }

    public void setName(String name) {

        this.name = name;
    }

    public String getLevel() {

        return level;
    }

    public void setLevel(String level) {

        this.level = level;
    }

    public String getType() {

        return type;
    }

    public void setType(String type) {

        this.type = type;
    }

    public String getMajor() {

        return major;
    }

    public void setMajor(String major) {

        this.major = major;
    }

    public String getInfoStart() {

        return infoStart;
    }

    public void setInfoStart(String infoStart) {

        this.infoStart = infoStart;
    }

    public String getInfoEnd() {

        return infoEnd;
    }

    public void setInfoEnd(String infoEnd) {

        this.infoEnd = infoEnd;
    }

    public String getInfoCorporate() {

        return infoCorporate;
    }

    public void setInfoCorporate(String infoCorporate) {

        this.infoCorporate = infoCorporate;
    }
}